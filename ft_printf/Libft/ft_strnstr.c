#include <stddef.h>

char	*ft_strnstr(const char *haystack, const char *needle, size_t len);

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	if (needle[i] == 0)
		return ((char *)haystack);
	while (haystack[i] != '\0' && i < len)
	{
		j = 0;
		if (haystack[i] == needle[j])
		{
			while (haystack[i] == needle[j] && haystack[i] != '\0')
			{
				i++;
				j++;
			}
			if (needle[j] == '\0' && i <= len)
				return (&((char *)haystack)[i - j]);
			i -= j;
		}
		i++;
	}
	return (0);
}
